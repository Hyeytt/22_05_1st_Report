# 연습문제 4

import turtle                    # turtle 불러오기
t = turtle .Turtle()             # turtle을 t로 대체하기
t.shape("turtle")                # 거북이 모양을 사용하겠다는 뜻
t.forward(100)                   # t를 100만큼 이동시키기
t.left(90)                       # t를 왼쪽으로 90도 회전시키기
t.forward(100)                   # t를 100만큼 이동
t.right(90)                      # t를 오른쪽으로 90도 회전시키기
t.forward(100)                   # t를 100만큼 이동시키기
t.right(90)                      # t를 오른쪽으로 90도 회전시키기
t.forward(100)                   # t를 100만큼 이동시키기
t.left(90)                       # t를 왼쪽으로 90도 회전시키기
t.forward(100)                   # t를 100만큼 이동시키기
t._screen.exitonclick()          # 화면을 클릭해야 종료
